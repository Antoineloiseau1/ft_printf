#include "ft_printf.h"

int	ft_printf(const char *format, ...)
{
	va_list		args;
	int		i;
	int		len;

	i = 0;
	len = 0;
	va_start(args, format);
	while (format[i])
	{
		if (format[i] == '%')
		{
			len += ft_cases(format[i + 1], args);
			i++;
		}
		else
			len += ft_putchar_len(format[i]);
		i++;
	}
	va_end(args);
	return (len);
}
